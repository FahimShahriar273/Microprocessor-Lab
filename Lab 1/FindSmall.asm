.MODEL SMALL
.STACK 100H
.DATA

.CODE
MAIN PROC


 
    
MOV AH,1
INT 21H
MOV BH,AL  

INT 21H
MOV BL,AL  

INT 21H
MOV CL,AL  



CMP CL,BH
JL CL_LESS_BH

CL_LESS_BH:
CMP CL,BH
JGE PASSCL
CMP CL,BL
JL CL_SMALL


PASSCL:

CMP BL,BH
JL BL_LESS_BH

BL_LESS_BH:
CMP BL,BH
JGE PASSBL
CMP BL,CL
JL BL_SMALL

PASSBL:


CMP BH,BL
JL BH_LESS_BL

BH_LESS_BL:
CMP BH,CL
JL BH_SMALL






    
BH_SMALL:
MOV AH,2
MOV DL,BH
INT 21H
JMP EXIT    

BL_SMALL:
MOV AH,2
MOV DL,BL
INT 21H
JMP EXIT    

CL_SMALL:
MOV AH,2
MOV DL,CL
INT 21H
JMP EXIT    


    
EXIT:    
    MAIN ENDP
END MAIN



